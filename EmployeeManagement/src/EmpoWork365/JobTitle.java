package EmpoWork365;

/**
 *
 * @author jenal
 */
public class JobTitle {
    private int id;
    private String title;

    public JobTitle(int id, String title) {
        this.id = id;
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    @Override
    public String toString() {
        return title; // This ensures the combo box displays the title
    }
}
